#Define the function sumDiff
def sumDiff(a, b):

    #return value 
    return (a + b) - (a - b)

#print the funtion in order to get 10
print (sumDiff (10, 5))

#print the funtion in order to get 4
print (sumDiff (17, 2))

#print the funtion in order to get 198
print (sumDiff (123, 99))